            var full = new byte[2048];
            System.Array.Copy(partial, full, partial.Length);
            for (int c = partial.Length / 8; c < 256; c++)
            {
                for (int r = 0; r < 8; r++)
                    full[c * 8 + r] = (byte)((r % 2 == 0) ? 0x00 : 0xFF);
            }
            return full;
        }
