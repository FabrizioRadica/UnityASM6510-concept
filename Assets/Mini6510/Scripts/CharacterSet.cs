        // Each character is 8 bytes (8x8 pixels)
        public const int CHAR_COUNT  = 256;
        public const int BYTES_PER_CHAR = 8;

        private readonly byte[] _builtIn;
        private readonly Memory _mem;

        public CharacterSet(Memory mem)
        {
            _mem     = mem;
            _builtIn = BuildROMCharset();
            // Copy built-in charset into Character Set RAM
            for (int i = 0; i < _builtIn.Length; i++)
                _mem.Write(Memory.CHARSET_START + i, _builtIn[i]);
        }

        // Returns the 8-byte bitmap for character code c from Character Set RAM
        public void GetCharBitmap(byte charCode, byte[] outBytes)
        {
            int addr = Memory.CHARSET_START + charCode * BYTES_PER_CHAR;
            for (int i = 0; i < BYTES_PER_CHAR; i++)
                outBytes[i] = _mem.Read(addr + i);
        }

